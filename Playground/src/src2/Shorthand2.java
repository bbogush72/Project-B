public class Shorthand2 {

	public static void main(String[] args) {
		
			int offerCount = 1;
			offerCount += 5;
				System.out.println(" offerCount is "+ offerCount );
			offerCount -= 2;
				System.out.println(" offerCount is "+ offerCount );
			offerCount *= 4;
				System.out.println(" offerCount is "+ offerCount );
			offerCount /= 2;
				System.out.println(" offerCount is "+ offerCount );
			offerCount %= 5;
				System.out.println(" offerCount is "+ offerCount );
						
		}

	}

