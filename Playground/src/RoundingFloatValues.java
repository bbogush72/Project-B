
public class RoundingFloatValues {

	public static void main(String[] args) {
		  // float numbers 
	      float x = 4567.9874f; 
	  
	      // find the closest int for these floats 
	      System.out.println(Math.round(x));
	      
	      
	      double y = 888.999;
	      
	      System.out.println(Math.round(y));

	}

}
