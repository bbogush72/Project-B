package day15;

public class NumberToChar {

	public static void main(String[] args) {
		
		int x = 100;
		char myChar =(char) x;
		
		System.out.println("Ascii of int value x represents ===> "+ myChar);
		
		//special character ==> number ==> upperCase ==> lowerCase
		
		double y = 100.99;
		char myChar2 = (char)y;
		System.out.println("Ascii of double value x represents ===> "+ myChar2);

	}

}
